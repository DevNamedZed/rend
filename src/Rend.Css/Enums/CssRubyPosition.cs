namespace Rend.Css
{
    /// <summary>
    /// CSS ruby-position property values (CSS Ruby Layout Module Level 1).
    /// </summary>
    public enum CssRubyPosition : byte
    {
        /// <summary>Ruby text appears above the base in horizontal mode, to the right in vertical.</summary>
        Over = 0,
        /// <summary>Ruby text appears below the base in horizontal mode, to the left in vertical.</summary>
        Under = 1
    }
}
